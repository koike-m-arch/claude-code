def make_weekly_row(start, end, d):
    if start == '合計':
        label_cells = ['', '合計', '', '']
    else:
        label_cells = ['', start, '〜', end]
    return label_cells + [
        g(d,0), g(d,1), g(d,2), g(d,3), g(d,4),
        g(d,5), g(d,6), g(d,7),
        g(d,8), g(d,9), g(d,10), g(d,11),
        g(d,12), g(d,13), g(d,14), g(d,15), g(d,16)
    ]

